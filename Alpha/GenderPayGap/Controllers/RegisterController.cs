using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using GenderPayGap.Models.GpgEntityModel;

namespace GenderPayGap.Controllers
{
    public class RegisterController : Controller
    {
        private GpgDBEntitiesContext db = new GpgDBEntitiesContext();

        // GET: Register
        public ActionResult Index()
        {
            return View(db.User.ToList());
        }

        // GET: Register/Details/5
        public ActionResult Details(long? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.User.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // GET: Register/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Register/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "UserId,UserRef,AuthProviderId,AuthUserTokenId,UserType,Title,Firstname,Lastname,Address1,Address2,Address3,TownCity,County,CountryCode,PostCode,WorkPhone1,WorkPhone2,HomePhone,Mobile,MobileVerifyCode,MobileVerifyDate,Fax,Web,EmailAddress,VerifyCode,VerifyDate,CurrentStatus,CurrentStatusDate,CurrentStatusDetails,Created,Modified")] User user)
        {
            if (ModelState.IsValid)
            {
                db.User.Add(user);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(user);
        }

        // GET: Register/Edit/5
        public ActionResult Edit(long? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.User.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // POST: Register/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "UserId,UserRef,AuthProviderId,AuthUserTokenId,UserType,Title,Firstname,Lastname,Address1,Address2,Address3,TownCity,County,CountryCode,PostCode,WorkPhone1,WorkPhone2,HomePhone,Mobile,MobileVerifyCode,MobileVerifyDate,Fax,Web,EmailAddress,VerifyCode,VerifyDate,CurrentStatus,CurrentStatusDate,CurrentStatusDetails,Created,Modified")] User user)
        {
            if (ModelState.IsValid)
            {
                db.Entry(user).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(user);
        }

        // GET: Register/Delete/5
        public ActionResult Delete(long? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.User.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // POST: Register/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(long id)
        {
            User user = db.User.Find(id);
            db.User.Remove(user);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
